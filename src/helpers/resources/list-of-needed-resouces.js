export const getOauth = () => {
  const token = localStorage.getItem('oauth');
  return {
    authorization: `Bearer ${token}`,
  };
};

export const getUserInfo = () => {
  const token = localStorage.getItem('oauth');
  return jwt.decode(token);
};
